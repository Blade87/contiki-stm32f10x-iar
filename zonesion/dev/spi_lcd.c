void Display_ASCII5X8(unsigned  int x0,unsigned  int y0,unsigned int co, unsigned char *s)
{
	unsigned char ch = *s;
	unsigned char dot;
	int i, j;
#define CWIDTH  5
	while (ch != 0) {
	 	if (ch<0x20 || ch > 0x7e) {
		  ch = 0x20;
		}
		ch -= 0x20;
		for (i=0; i<5; i++) {
			dot = nAsciiDot[ch*5+i];
			for (j=0; j<8; j++) {
				if (dot&0x80)Output_Pixel(x0+i, y0+j, co);	
				dot <<= 1;
			}
		}
		x0 += CWIDTH;
_next:
		ch = *++s;	
	}
}

