/*******************************************************************************
* Function Name  : SPI_LCD_Init
* Description    : Initializes the peripherals used by the SPI FLASH driver.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void SPI_LCD_Init(void)
{
  SPI_InitTypeDef  SPI_InitStructure;
  GPIO_InitTypeDef GPIO_InitStructure;

#if USE_SPI
  /* Enable LCD_SPIx and GPIO clocks */
  RCC_APB1PeriphClockCmd(RCC_APB1Periph_SPI2, ENABLE);
#endif

  RCC_APB2PeriphClockCmd( RCC_APB2Periph_GPIO_RS |RCC_APB2Periph_GPIO_REST |
                         RCC_APB2Periph_GPIO_CS, ENABLE);
#if USE_SPI
  /* Configure LCD_SPIx pins: SCK, MISO and MOSI */
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15 /*| GPIO_Pin_14  miso*/ | GPIO_Pin_13;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_10MHz;
  GPIO_Init(GPIOB, &GPIO_InitStructure);
#else
   /* Configure LCD_SPIx pins: SCK, MISO and MOSI */
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_15 /*| GPIO_Pin_14  miso*/ | GPIO_Pin_13;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_10MHz;
  GPIO_Init(GPIOB, &GPIO_InitStructure);
  SPI_LCD_CLK(1);
     
#endif
	//REST 
   GPIO_InitStructure.GPIO_Pin = GPIO_Pin_REST;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
  GPIO_Init(GPIO_REST, &GPIO_InitStructure);

	// rs pin config
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_RS;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
  GPIO_Init(GPIO_RS, &GPIO_InitStructure);

  /* Configure I/O for Flash Chip select */
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_CS;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP;
  GPIO_Init(GPIO_CS, &GPIO_InitStructure);

  /* Deselect the FLASH: Chip Select high */
  SPI_LCD_CS_HIGH();

 #if USE_SPI
  /* LCD_SPIx configuration */
  SPI_InitStructure.SPI_Direction = SPI_Direction_1Line_Tx;
  SPI_InitStructure.SPI_Mode = SPI_Mode_Master;
  SPI_InitStructure.SPI_DataSize = SPI_DataSize_8b;
  SPI_InitStructure.SPI_CPOL = SPI_CPOL_High;
  SPI_InitStructure.SPI_CPHA = SPI_CPHA_2Edge;
  SPI_InitStructure.SPI_NSS = SPI_NSS_Soft;
  SPI_InitStructure.SPI_BaudRatePrescaler = SPI_BaudRatePrescaler_4;
  SPI_InitStructure.SPI_FirstBit = SPI_FirstBit_MSB;
  SPI_InitStructure.SPI_CRCPolynomial = 7;
  SPI_Init(LCD_SPIx, &SPI_InitStructure);

  /* Enable LCD_SPIx  */
  SPI_Cmd(LCD_SPIx, ENABLE);
#endif
}


void write_command(unsigned char c)
{
  SPI_LCD_RS_LOW();

  SPI_LCD_CS_LOW();

  /* Send "Write Enable" instruction */
#if USE_SPI
  /* Send byte thROTATIONugh the LCD_SPIx peripheral */
  SPI_I2S_SendData(LCD_SPIx, c);
    /* Loop while DR register in not emplty */
  while (SPI_I2S_GetFlagStatus(LCD_SPIx, SPI_I2S_FLAG_TXE) == RESET);
#else
{	
	int i;
	for (i=0; i<8; i++) {
		SPI_LCD_DAT(0x01&(c>>(7-i)));
		SPI_LCD_CLK(0);
		SPI_LCD_CLK(1);	
	}
}
#endif

  /* Deselect the FLASH: Chip Select high */
  SPI_LCD_CS_HIGH();	
}

void write_data(unsigned char c)
{
  SPI_LCD_RS_HIGH();

  SPI_LCD_CS_LOW();
#if USE_SPI
  /* Send byte thROTATIONugh the LCD_SPIx peripheral */
  SPI_I2S_SendData(LCD_SPIx, c);
    /* Loop while DR register in not emplty */
  while (SPI_I2S_GetFlagStatus(LCD_SPIx, SPI_I2S_FLAG_TXE) == RESET);
#else
{	
	int i;
	for (i=0; i<8; i++) {
		SPI_LCD_DAT(0x01&(c>>(7-i)));
		SPI_LCD_CLK(0);
		SPI_LCD_CLK(1);	
	}
}
#endif
  /* Deselect the FLASH: Chip Select high */
  SPI_LCD_CS_HIGH();	
}

